# data_types.py
# This file demonstrates common Python data types

# Integer
marks = 95
print("Marks:", marks, type(marks))

# Float
percentage = 89.5
print("Percentage:", percentage, type(percentage))

# String
message = "Learning Python"
print("Message:", message, type(message))

# Boolean
is_passed = True
print("Passed:", is_passed, type(is_passed))

# List (collection of multiple values)
numbers = [1, 2, 3, 4, 5]
print("Numbers List:", numbers, type(numbers))

# Tuple (immutable list)
coordinates = (10, 20)
print("Coordinates:", coordinates, type(coordinates))

